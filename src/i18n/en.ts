type PathsOf<T> = {
  [K in keyof T & string]: T[K] extends string
  ? K
  : T[K] extends object
  ? `${K}.${PathsOf<T[K]>}`
  : never
}[keyof T & string];

export type TranslationPath = PathsOf<typeof en>;
